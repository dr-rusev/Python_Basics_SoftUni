# Напишете програма, която да отпечатва часовете в денонощието от 0:0:0 до 23:59:59,
# всеки на отделен ред. Часовете да се изписват във формат "{час} : {минути} : {секунди} ".
for i in range(0, 24):
    hour = i
    for j in range(0, 60):
        minutes = j
        for k in range(0, 60):
            seconds = k
            print(f"{hour} : {minutes} : {seconds}")