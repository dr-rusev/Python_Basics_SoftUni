# и ден от седмицата(текст) - въведени от потребителя и проверява
# дали офисът на фирма е отворен, като работното време на
# офиса е от 10-18 часа, от понеделник до събота включително
# Изхдът е печат на open или closed
hour = int(input())
day = input()
if 18 < hour or hour < 10 or day == "Sunday":
    print('closed')
else:
    print('open')